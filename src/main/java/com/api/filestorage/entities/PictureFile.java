package com.api.filestorage.entities;

public class PictureFile {
	private int width;
	private int height;

	public PictureFile() {
	}

	public int getWidth() {
		return this.width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return this.height;
	}

	public void setHeight(int height) {
		this.height = height;
	}


}
